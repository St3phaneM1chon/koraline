#!/usr/bin/env npx tsx
/**
 * AUDIT FORGE вЂ” Monthly Audit Orchestrator
 * Cross-module + compliance + E2E + performance baseline.
 *
 * Usage: npx tsx scripts/mega-audit/monthly-audit.ts
 */

import { loadAllDomainFindings, analyzeCrossModule, generateCrossModuleReport } from './cross-module-check';
import { generateScoreDashboard, generateLifecycleSummary } from './scoring';
import { saveMonthlyReport, getLatestBaseline, detectRegressions, ensureDirectories } from './historical-tracker';
import { calculateDomainScore, calculateGlobalScore, DOMAINS, Grade } from './audit-config';
import * as fs from 'fs';
import * as path from 'path';

console.log('рџ”Ќ AUDIT FORGE вЂ” Monthly Audit');
console.log(`рџ“… Date: ${new Date().toISOString().slice(0, 10)}`);
console.log('');

// в”Ђв”Ђ Step 1: Load all domain findings в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const domainFindings = loadAllDomainFindings();
const auditedDomains = Object.keys(domainFindings);

console.log(`рџ“Љ Loaded findings from ${auditedDomains.length} domains:`);
for (const [domain, findings] of Object.entries(domainFindings)) {
  console.log(`   ${domain}: ${findings.length} findings`);
}
console.log('');

// в”Ђв”Ђ Step 2: Cross-module analysis (Pass 3) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log('рџ”— Running cross-module analysis...');
const crossFindings = analyzeCrossModule(domainFindings);
const crossReport = generateCrossModuleReport(crossFindings);
console.log(crossReport);
console.log('');

// в”Ђв”Ђ Step 3: Domain scores в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const domainScores: Record<string, { score: number; grade: Grade; findingsCount: number; functionsAudited: number }> = {};
const scoreValues: Record<string, number> = {};

for (const [domain, findings] of Object.entries(domainFindings)) {
  const { score, grade } = calculateDomainScore(findings, findings.length || 1);
  domainScores[domain] = { score, grade, findingsCount: findings.length, functionsAudited: findings.length };
  scoreValues[domain] = score;
}

// в”Ђв”Ђ Step 4: Global score в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const global = calculateGlobalScore(scoreValues);
console.log(`\nрџЏ† Global Platform Score: ${global.score}/100 (${global.grade})`);

// в”Ђв”Ђ Step 5: Comparison with previous в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const previousScores = getLatestBaseline();
const previousScoreValues: Record<string, number> | undefined = previousScores
  ? Object.fromEntries(
      Object.entries(previousScores).map(([k, v]) => [k, typeof v === 'object' ? v.score : v])
    )
  : undefined;

const dashboard = generateScoreDashboard(domainScores, previousScoreValues);
console.log('\n' + dashboard);

// в”Ђв”Ђ Step 6: Regression detection в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const regressions = detectRegressions();
if (regressions.length > 0) {
  console.log('\nвљ пёЏ  REGRESSIONS DETECTED:');
  for (const r of regressions) {
    console.log(`   ${r.domain}: ${r.previousScore} в†’ ${r.currentScore} (${r.delta})`);
  }
}

// в”Ђв”Ђ Step 7: Lifecycle summary в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const allFindings = Object.values(domainFindings).flat();
const lifecycle = generateLifecycleSummary(allFindings);
console.log('\n' + lifecycle);

// в”Ђв”Ђ Step 8: Save monthly report в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

ensureDirectories();
const date = new Date().toISOString().slice(0, 10);

const reportPath = saveMonthlyReport({
  date,
  domainScores: Object.fromEntries(
    Object.entries(domainScores).map(([k, v]) => [k, { score: v.score, grade: v.grade }])
  ),
  globalScore: global.score,
  globalGrade: global.grade,
  totalFindings: allFindings.length,
});

// Save full report as markdown
const reportMd = [
  `# Audit Forge вЂ” Monthly Report (${date})`,
  '',
  dashboard,
  '',
  crossReport,
  '',
  lifecycle,
  '',
  '---',
  `*Generated by Audit Forge v5.0*`,
].join('\n');

const mdPath = path.join(path.dirname(reportPath), `${date.slice(0, 7)}-monthly-report.md`);
fs.writeFileSync(mdPath, reportMd);

console.log(`\nрџ“„ Report saved: ${reportPath}`);
console.log(`рџ“„ Markdown: ${mdPath}`);

